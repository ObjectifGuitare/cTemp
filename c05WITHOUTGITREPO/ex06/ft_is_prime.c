int	ft_is_prime(int nb)
{
	int	div;

	div = 2;
	if (nb <= 1)
		return (0);
	if (!(nb % div))
		return (0);
	div++;
	while (div <= nb / div)
	{
		if (!(nb % div))
			return (0);
		div += 2;
	}
	return (1);
}
